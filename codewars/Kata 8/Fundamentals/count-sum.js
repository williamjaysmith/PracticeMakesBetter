function countPositivesSumNegatives(input) {
    if (!input || input.length === 0) return [];
    let positives = [];
    let negatives = [];
    input.forEach((element) => {
        if (element > 0) {
            positives.push(element);
        } else if (element < 0) {
            negatives.push(element);
        }
    });
    let count = positives.length;
    let sum = negatives.reduce((a, b) => {
        return a + b;
    }, 0);
    return [count, sum];
}

