function expandedForm(num) {
    let numArray = num.toString().split("");

    let expandedArray = numArray.map((digit, index, array) => {
        let placeValue = digit * Math.pow(10, array.length - index - 1);

        return placeValue;
    });

    return expandedArray.filter((val) => val > 0).join(" + ");
}

console.log(expandedForm(70304));

