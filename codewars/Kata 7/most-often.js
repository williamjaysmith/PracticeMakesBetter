function countChar(string) {
    let charMap = {};
    let maxValue = 0;
    let maxChar = "";

    for (char of string) {
        charMap[char] = (charMap[char] || 0) + 1;
    }

    for (let key in charMap) {
        if (charMap[key] > maxValue) {
            maxValue = charMap[key];
            maxChar = key;
        }
    }
    return maxChar;
}

