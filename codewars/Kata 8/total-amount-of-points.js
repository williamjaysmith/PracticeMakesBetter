function points(games) {
    let seasonTotal = 0;

    for (let i = 0; i < games.length; i++) {
        games[i][0] == games[i][2]
            ? (seasonTotal += 1)
            : games[i][0] > games[i][2]
            ? (seasonTotal += 3)
            : 0;
    }
    return seasonTotal;
}

