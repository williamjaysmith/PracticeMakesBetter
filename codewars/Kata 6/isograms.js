function isIsogram(str) {
    const strMap = {};
    for (let char of str.toLowerCase()) {
        if (strMap[char]) {
            return false;
        } else {
            strMap[char] = true;
        }
    }
    return true;
}

console.log(isIsogram("Dermatoglyphics"), isIsogram("aba"));

