function capitalize(s) {
    let capEvens = s
        .split("")
        .map((char, i) => (i % 2 === 0 ? char.toUpperCase() : char))
        .join("");
    let capOdds = s
        .split("")
        .map((char, i) => (i % 2 !== 0 ? char.toUpperCase() : char))
        .join("");

    return [capEvens, capOdds];
}

